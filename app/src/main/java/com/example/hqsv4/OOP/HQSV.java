package com.example.hqsv4.OOP;

public class HQSV {
    private int Hinh;
    private String Ten;

    public HQSV(int hinh, String ten) {
        Hinh = hinh;
        Ten = ten;
    }

    public int getHinh() {
        return Hinh;
    }

    public void setHinh(int hinh) {
        Hinh = hinh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}
